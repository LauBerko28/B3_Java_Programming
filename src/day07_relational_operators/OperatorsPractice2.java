package day07_relational_operators;

public class OperatorsPractice2 {
    public static void main(String[] args) {
         int a = 40; // 39 - 38- 37 - 36 - 35 - 34

        System.out.println(--a); // update then use
        System.out.println(--a); // update then use
        System.out.println(a--); // use then update
        System.out.println(--a); // update then use
        System.out.println(--a); // update then use
        System.out.println(a--); // use then update

        /*
        Output so far:
        39
        38
        37
        36
        35
        34
         */
    }
}
