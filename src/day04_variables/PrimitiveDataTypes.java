package day04_variables;

public class PrimitiveDataTypes {
    public static void main (String [ ]args){

        byte age = 60;
        System.out.println("age"); // will print the word age
        System.out.println(age); // datatype here is byte
        System.out.println(60); //datatype is int

        // byte age2 =200 is too big for byte datatype

        short year; // declared a variable called year which has datatype short
        year = 2024; // assigned a value, 2024, into my variable called year
        System.out.println(year); // datatype here is short
        System.out.println(2024); // datatype here is int

        int addressNumber = 244432;
        System.out.println(addressNumber); // datatype here is int
        System.out.println(244432); // datatyoe is int

        long bigNumber; // datatype here is long
        bigNumber = 95346634656333L; /* Always add the letter L at the end of the number
        datatype here is long */




    }
}
